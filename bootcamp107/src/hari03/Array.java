package hari03;

public class Array {
	public static void main(String[] args) {
		int[] angka = new int[5];
		// mengisi array
		angka[0] = 1;
		angka[1] = 3;
		angka[2] = 5;
		angka[3] = 7;
		angka[4] = 9;
		
		//menampilkannya
		System.out.println(angka[0]);
		System.out.println(angka[1]);
		System.out.println(angka[2]);
		System.out.println(angka[3]);
		System.out.println(angka[4]);

	}
}
